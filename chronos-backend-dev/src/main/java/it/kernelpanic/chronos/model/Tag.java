package it.kernelpanic.chronos.model;


/**
 * Tag model to generates tag for the single classification of events created
 * by user
 */
public class Tag {
    private long id;
    private String name;

    public Tag() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Tag{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
